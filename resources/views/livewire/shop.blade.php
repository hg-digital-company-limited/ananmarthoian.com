                                        parentListItem.removeClass('image-selected');
                                        $('.wrapper-quickview').removeClass('is-visible-info').velocity({
                                            'top': finalTop + 'px',
                                            'left': finalLeft + 'px',
                                            'width': finalWidth + 'px',
                                        }, 300, 'ease', function() {
